#include "../libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*tmp;
	int		ct;

	ct = 0;
	tmp = lst;
	while (tmp != NULL)
	{
		tmp = tmp->next;
		ct++;
	}
	return (ct);
}
