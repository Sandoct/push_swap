#include "libft.h"

int	ft_isnumber(char *str)
{
	if (*str == '-' || *str == '+')
		if (ft_strlen(str++) == 1)
			return (0);
	while (*str)
		if (!ft_isdigit(*str++))
			return (0);
	return (1);
}
